namespace NHINDirect.Diagnostics
{
    ///<summary>
    /// Defines available log levels.
    ///</summary>
    public enum LoggingLevel
    {
        ///<summary>
        /// The Off level.
        ///</summary>
        Off = 1,
        ///<summary>
        /// The Fatal level.
        ///</summary>
        Fatal,
        ///<summary>
        /// The Error level.
        ///</summary>
        Error,
        ///<summary>
        /// The Warn level.
        ///</summary>
        Warn,
        ///<summary>
        /// The Info level.
        ///</summary>
        Info,
        ///<summary>
        /// The Debug level.
        ///</summary>
        Debug,
        ///<summary>
        /// The Trace level.
        ///</summary>
        Trace,
    }
}