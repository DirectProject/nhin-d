package org.nhindirect.policy;

public class PolicyParseException extends Exception
{
	static final long serialVersionUID = -7479680642491131294L;

   /////CLOVER:OFF
	/**
	 * {@inheritDoc}
	 */
    public PolicyParseException() 
    {
    }

	/**
	 * {@inheritDoc}
	 */
    public PolicyParseException(String msg) 
    {
        super(msg);
    }

	/**
	 * {@inheritDoc}
	 */
    public PolicyParseException(String msg, Throwable t) 
    {
        super(msg, t);
    }

	/**
	 * {@inheritDoc}
	 */
    public PolicyParseException(Throwable t) 
    {
        super(t);
    }
    /////CLOVER:ON
}
